import java.util.Scanner;
class fibonacci_series
 {
  public static void main(String args[]) 
  {
    Scanner br = new Scanner(System.in);
    int terms,a=0,b=1,sum=0;
    System.out.println("Enter Number of terms you want to print in fibonacci series :");
    terms= br.nextInt();
    do{
      System.out.print(a+",");
      sum=a+b;
      a=b;
      b=sum;
      terms--;
    }while(terms>0);
  }
}
